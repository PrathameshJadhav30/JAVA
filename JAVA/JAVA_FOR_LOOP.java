public class JAVA_FOR_LOOP {
    public static void main(String[] args)
    {
       //2i = EVEN NUMBER = 0,2,4,6,8,.....
       //2i+1 = ODD NUMBERS = 1,3,5,7,9,....
       int n = 50;
       for(int i =0; i<50;i++)
       {
           System.out.println(2*i+1);
       }

       for(int i =5;i>0;i--)
       {
           System.out.println(i);
       }
    }

}

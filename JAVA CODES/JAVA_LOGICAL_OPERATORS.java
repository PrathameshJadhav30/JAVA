public class JAVA_LOGICAL_OPERATORS {
    public static void main(String[] args)
    {
        System.out.println("for logical AND");
        boolean a = true;
        boolean b = false;
        if (a && b)
        {
            System.out.println("Y");
        }
        else {
            System.out.println("N");
        }
        System.out.println("for logical OR");
        boolean c = true;
        boolean d = false;
        if (c || d)
        {
            System.out.println("Y");
        }
        else {
            System.out.println("N");
        }
    }
}
